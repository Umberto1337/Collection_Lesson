# Методы upper(), lower(), title(), capitalize()
# Изменение регистра
text = 'однажды в СТУДЁНУЮ зИмнЮЮ ПоРУ'
print(text.upper()) # Верхний регистр всех элементов строки
print(text.lower()) # Нижний регистр всех элементов строки
print(text.title()) # Заглавная буква каждого элемента после пробела
print(text.capitalize()) # Заглавная буква первого элемента начало строки


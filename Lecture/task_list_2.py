# Квадратные скобки [ ]
# Доступ к элементу по индексу

my_list = [2, 4, 6, 8, 10, 12]

print(my_list[0]) # индексация с начала списка 0 = 2
print(my_list[6]) # Выход за рамки списка IndexError

print(my_list[-1]) # индексация с конца списка -1 = 12
print(my_list[-10]) # Выход за рамки списка IndexError
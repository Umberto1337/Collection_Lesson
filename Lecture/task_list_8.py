# Метод insert()
# Вставка элемента по индексу
my_list = [2, 4, 6, 8, 10, 12]
my_list.insert(2, 555) # позволяет вставить элемент по индексу(куда вставить) второй сам элемент который нужно добавить.
print(my_list)
my_list.insert(-2, 13)
print(my_list)
my_list.insert(42, 73) # my_list.append(73)
print(my_list)
